package FlyWeight;

public class Tree {

    private TreeType treeType;
    private int x;
    private int y;

    public Tree(int x, int y, TreeType treeType) {
        this.x = x;
        this.y = y;
        this.treeType = treeType;
    }

    public TreeType getTreeType() {
        return treeType;
    }

    public void setTreeType(TreeType treeType) {
        this.treeType = treeType;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void draw(){
        System.out.println("Drawing tree of type " + treeType.getName() + " at (" + x + ", " + y + ")");
    }
}
